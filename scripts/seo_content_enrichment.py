def enrich_page(page: dict) -> dict:
    """Return a copy of page with merged enrichment content."""
    slug = page.get("slug")
    if not slug or slug not in PAGE_ENRICHMENTS:
        return page
    data = PAGE_ENRICHMENTS[slug]
    enriched = dict(page)
    if data.get("sections"):
        enriched["sections"] = list(page.get("sections", [])) + data["sections"]
    if data.get("faqs"):
        enriched["faqs"] = list(page.get("faqs", [])) + data["faqs"]
    if data.get("hub_links_extra") and page.get("hub_links"):
        enriched["hub_links"] = list(page["hub_links"]) + data["hub_links_extra"]
    return enriched
